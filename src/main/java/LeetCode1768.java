/**
 * <a href="https://leetcode.com/problems/merge-strings-alternately">1768. Merge Strings Alternately</a>
 */
public class LeetCode1768 {

    /**
     * Approach one - Using two pointers technique
     */
    public String approachOne(String word1, String word2) {
        int m = word1.length();
        int n = word2.length();
        StringBuilder result = new StringBuilder();
        int i = 0, j = 0;

        while (i < m || j < n) {
            if (i < m) {
                result.append(word1.charAt(i++));
            }
            if (j < n) {
                result.append(word2.charAt(j++));
            }
        }

        return result.toString();
    }

    /**
     * Approach Two - Using one pointer technique
     */
    public String approachTwo(String word1, String word2) {
        int m = word1.length();
        int n = word2.length();
        StringBuilder result = new StringBuilder();

        for (int i = 0; i < Math.max(m, n); i++) {
            if (i < m) {
                result.append(word1.charAt(i));
            }
            if (i < n) {
                result.append(word2.charAt(i));
            }
        }

        return result.toString();
    }

}
